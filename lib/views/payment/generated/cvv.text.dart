class CVVText extends StatefulWidget {
  @override
  State<CVVText> createState() => _CVVTextState();
}

class _CVVTextState extends State<CVVText> {
  final _cvv = TextEditingController();

  bool labelHidden = false;

  @override
  Widget build(BuildContext context) {
    final validationProvider =
        Provider.of<PaymentValidationProvider>(context, listen: true);
    final theme = Provider.of<ThemeProvider>(context);
    return Scaffold(
      backgroundColor: theme.isDarkMode
          ? Color(0xff67748E)
          : Color.fromARGB(255, 255, 255, 255),
      body: Center(
        child: Material(
          color: theme.isDarkMode ? Color(0xff67748E) : Colors.white,
          child: TextFormField(
            controller: _cvv,
            decoration: InputDecoration(
                errorText: validationProvider.cvvNumIsValid != null &&
                        !validationProvider.cvvNumIsValid!
                    ? 'Must be numbers and 3 digit!'
                    : null,
                errorStyle: TextStyle(
                    color: theme.isDarkMode ? Colors.tealAccent : Colors.red),
                border: InputBorder.none,
                filled: true,
                fillColor: theme.isDarkMode ? Color(0xff67748E) : Colors.white,
                labelText: labelHidden ? null : ('CVV'),
                labelStyle:
                    TextStyle(color: theme.isDarkMode ? white : Colors.black)),
            style: TextStyle(color: theme.isDarkMode ? white : Colors.black),
            onChanged: (String value) {
              Provider.of<PaymentValidationProvider>(context, listen: false)
                  .validateCvv(value);
              if (value.isEmpty) {
                labelHidden = false;
              } else {
                labelHidden = true;
              }
            },
          ),
        ),
      ),
    );
  }
}
