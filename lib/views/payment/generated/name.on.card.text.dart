class CardNameText extends StatefulWidget {
  @override
  State<CardNameText> createState() => _CardNameTextState();
}

class _CardNameTextState extends State<CardNameText> {
  final _cardname = TextEditingController();

  bool labelHidden = false;

  @override
  Widget build(BuildContext context) {
    final validationProvider =
        Provider.of<PaymentValidationProvider>(context, listen: true);
    final theme = Provider.of<ThemeProvider>(context);
    return Scaffold(
      body: Center(
        child: Material(
          color: theme.isDarkMode ? Color(0xff67748E) : Colors.white,
          child: TextField(
            controller: _cardname,
            decoration: InputDecoration(
                errorText: validationProvider.cardNameIsValid != null &&
                        !validationProvider.cardNameIsValid!
                    ? 'Must not be Empty!'
                    : null,
                errorStyle: TextStyle(
                    color: theme.isDarkMode ? Colors.tealAccent : Colors.red),
                border: InputBorder.none,
                filled: true,
                fillColor: theme.isDarkMode ? Color(0xff67748E) : Colors.white,
                labelText: labelHidden ? null : ('Card Name'),
                labelStyle:
                    TextStyle(color: theme.isDarkMode ? white : Colors.black)),
            style: TextStyle(color: theme.isDarkMode ? white : Colors.black),
            onChanged: (String value) {
              Provider.of<PaymentValidationProvider>(context, listen: false)
                  .validateCardName(value);
              if (value.isEmpty) {
                labelHidden = false;
              } else {
                labelHidden = true;
              }
            },
          ),
        ),
      ),
    );
  }
}
